def make_request(url, headers, method):
    try:
        resp = requests.request(method, url, headers=headers, timeout=8, verify=False)
        return resp, resp.content.decode(errors='ignore')
    except Exception as e:
        print(Fore.RED + f"[!] Error: {e}")
        return None, None

