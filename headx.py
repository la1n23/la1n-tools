def repeater(url, header, value, method):
    time.sleep(random.uniform(0.3, 0.8))  # Reduce delay for faster execution
    
    initial_resp, init_content = make_request(url, {}, method)
    if not initial_resp:
        return
    
    initial_hash = hashlib.sha256(init_content.encode('utf-8')).hexdigest()
    
    # Second request (checks natural content variability)
    second_resp, second_content = make_request(url, {}, method)
    if not second_resp:
        return
    
    dynamic_content = initial_hash != hashlib.sha256(second_content.encode('utf-8')).hexdigest()
    
    # Third request (with injected header)
    headers = {header: value}
    modified_resp, modified_content = make_request(url, headers, method)
    if not modified_resp:
        return
    
    modified_hash = hashlib.sha256(modified_content.encode('utf-8')).hexdigest()
    
    # Change detection
    status_changed = modified_resp.status_code != initial_resp.status_code
    hash_diff = modified_hash != initial_hash if not dynamic_content else False
    
    # Reflection Detection
    reflection = False
    reflections = {"headers": [], "content": False}

    for resp_header, header_value in modified_resp.headers.items():
        if value in header_value:
            reflection = True
            reflections['headers'].append(resp_header)
    
    if value in modified_content:
        reflections['content'] = True
        reflection = True
    
    # Prepare the result for logging
    result = {
        "target": url,
        "header": header,
        "value": value,
        "status_code": modified_resp.status_code,
        "content_sha256": modified_hash,
        "reflection_detected": reflection,
        "reflection_details": reflections,
        "status_changed": status_changed,
        "hash_diff": hash_diff,
        "initial_request": {
            "status_code": initial_resp.status_code,
            "content_sha256": initial_hash,
            "initial_content": init_content[:200]  #
        }
    }
    
    # Print only if a change is detected
    if status_changed or hash_diff:
        color = Fore.GREEN if 200 <= modified_resp.status_code < 300 else Fore.YELLOW if modified_resp.status_code in [401, 403] else Fore.BLUE
        print(Fore.GREEN + '{ Alhamdulellah }', Fore.CYAN + '[ Resp Changed ]',
              Fore.RESET + 'Target:', Fore.MAGENTA + url,
              "Header:", Fore.MAGENTA + f"{header}: {value}",
              "StatusCode:", color, modified_resp.status_code,
              Fore.BLUE + "Content SHA256:", Fore.RESET, modified_hash)

        if reflection:
            msg = "Headers: " + str(reflections['headers']) if reflections['headers'] else ""
            msg += " Check Content" if reflections['content'] else ""
            print(Fore.GREEN + '{ Alhamdulellah }', Fore.RED + "[ REFLECTION DETECTED ]", Fore.CYAN + f"{url} - ", Fore.RED + header, Fore.CYAN + msg)
        
        # Log the result to a JSON file
        log_to_file(result)
def scanner(url, method, ip_value, threads):
    headers = headers_organizer(ip_value)
    with ThreadPoolExecutor(max_workers=threads) as executor:
        for header, value in headers.items():
            executor.submit(repeater, url, header, value, method)

